
abstract class HAS {

	 boolean on,off;

	

	 public boolean isOn() {
			return on;
		}

		public void setOn() {
			this.on = true;
			this.off=false;
		}

		public boolean isOff() {
			return off;
		}

		public void setOff() {
			this.off = true;
			this.on=false;
		}

}
